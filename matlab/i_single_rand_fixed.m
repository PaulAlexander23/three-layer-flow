function h = i_single_rand_fixed(x,a)
    h = 0 * x;
    h = h + 0.241691285913833*cos(1*x + 0.241691285913833*2.0*pi);
    h = h + 0.403912145588115*cos(2*x + 0.403912145588115*2.0*pi);
    h = h + 0.096454525168389*cos(3*x + 0.096454525168389*2.0*pi);
    h = h + 0.131973292606335*cos(4*x + 0.131973292606335*2.0*pi);
    h = h / max(abs(h));
    h = a * h;
end