function h = i_double_rand_fixed(x, a, xL)

    r = [0.6020    0.2630    0.6541    0.6892    0.7482    0.4505    0.0838    0.2290    0.9133    0.1524];
    t = [5.1888    3.3825    6.2589    0.4912    2.7814    0.6701    6.0438    0.0291    4.8689    5.1353];

    h = (r.*cos( 2*pi/xL *(x*[1:5,1:5] + t)))*kron(eye(2),ones(5,1));
    h = a * h ./ max(max(abs(h)));
    h = [h(:,1);h(:,2)];
end